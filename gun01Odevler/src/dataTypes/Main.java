public class Main {
    public static void main(String[] args) {
        int number = 146;
        System.out.println("number = " + number);

        double avarage = 49.3; // Eyvah Kaldi bir puanla
        System.out.println("avarage = " + avarage);

        char letter = 'S';
        System.out.println("letter = " + letter);

        boolean areYouSure = false;
        System.out.println("areYouSure = " + areYouSure);

        System.out.println("Integer.MAX_VALUE = " + Integer.MAX_VALUE); // integerin en yuksek degeri
        System.out.println("Integer.MIN_VALUE = " + Integer.MIN_VALUE); // integerin en dusuk degeri

        System.out.println("Byte.MAX_VALUE = " + Byte.MAX_VALUE); // byte nin en yuksek degeri
        System.out.println("Byte.MIN_VALUE = " + Byte.MIN_VALUE); // byte nin en dusuk degeri
    }
}
